#ifndef _TEXTURE_HPP_
#define _TEXTURE_HPP_

#include <glad/glad.h>
#include <GL/gl.h>
#include "../Image/PixelTraits.hpp"
#include "../Image/PixelBufferTraits.hpp"
#include "../Image/RawImageTraits.hpp"

class Texture
{
    public:
        // Constructor
        Texture();

        // Destructor
        ~Texture();

        // Retrieves the internal opengl handle
        GLuint Id() const;

        // Sets the texture data
        template <typename PixelBuffer>
        void SetData(const PixelBuffer& pb);

    private:
        GLuint mId;
};

template <typename PixelBuffer>
void Texture::SetData(const PixelBuffer& pb)
{
    glBindTexture(GL_TEXTURE_2D, mId);
    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    GLint format = PixelTraits<typename PixelBufferTraits<PixelBuffer>::Pixel>::GetChannels() == 4 ? GL_RGBA : GL_RGB;
    uint32_t width = PixelBufferTraits<PixelBuffer>::Width(pb);
    uint32_t height = PixelBufferTraits<PixelBuffer>::Height(pb);
    const GLvoid* data = PixelBufferTraits<PixelBuffer>::GetData(pb);

    glTexImage2D(GL_TEXTURE_2D, 0, format, width, height, 0, format, GL_UNSIGNED_BYTE, data);

    glGenerateMipmap(GL_TEXTURE_2D);
    glBindTexture(GL_TEXTURE_2D, 0);
}

#endif // ! _TEXTURE_HPP_
