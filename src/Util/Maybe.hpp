#ifndef _MAYBE_HPP_
#define _MAYBE_HPP_

#include <string>

template <typename T>
class Maybe
{
    public:
        T data;
        bool valid;

        bool operator==(const Maybe<T>& rhs) const
        {
            return this->data == rhs.data;
        }
};

namespace std
{
    template<typename T>
    std::string to_string(const Maybe<T>& m)
    {
        return m.valid ? std::to_string(m.data) : std::string("");
    }
}

#endif // ! _MAYBE_HPP_
