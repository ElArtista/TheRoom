#ifndef _UUID_HPP_
#define _UUID_HPP_

#include <string>

/**
 * rUUID - A class for the representation of Universal Unique IDentifiers
 */
class rUUID
{
    public:
        // Default constructor
        rUUID();

        // Copy constructor
        rUUID(const rUUID& rhs);

        // Assignment operator
        void operator=(const rUUID& rhs);

        // Equality operator
        bool operator==(const rUUID& rhs) const;

        // Leq than operator
        bool operator<(const rUUID& rhs) const;

        // Returns the stored string representation of the UUID in a c string form
        const char* c_str() const;

        // Implements the cast operator char* so we can use the UUID object as a C string
        operator const char*() const;

        // Side constructor
        friend bool StringToUUID(const char* str, rUUID& uuid);

    private:
        // Stores the generated uuid
        std::string mUUID;
};

bool StringToUUID(const char* str, rUUID& uuid);

#endif // ! _UUID_HPP_
